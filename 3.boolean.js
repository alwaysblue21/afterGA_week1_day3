// === checks for equal value and equal type
console.log(1 === 1)

// == checks for equal value 
console.log("1" == 1)


// > greater then check
// >= "greater than or equal" to check
console.log(1 > 1) 
console.log(1 >= 1)

// < less then check
// <= "less than or equal" to check
console.log(-1 < -1) 
console.log(-1 <= -1)

// !== check for NON equality (for type and value)
console.log(1 !== 1)
console.log(1 !== 2)

// != check for NON equality (for value)
console.log(1 != 1)
console.log(1 != 2)
console.log('1' != 1)
console.log('1' != 2)

